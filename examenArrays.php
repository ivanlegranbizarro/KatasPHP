1. Crea un array con los números del 1 al 5 y luego elimina el elemento central. Muestra el array resultante.

2. Dado el siguiente array asociativo:
```php
$frutas = [
"manzana" => 3,
"plátano" => 2,
"naranja" => 4,
"pera" => 1
];
```
Ordena el array por valores de mayor a menor y muestra el resultado.

3. Crea una función que reciba un array de números y devuelva un nuevo array solo con los números que aparecen más de una vez.

4. Dado el siguiente array multidimensional:
```php
$estudiantes = [
["nombre" => "Ana", "notas" => [7, 8, 6]],
["nombre" => "Juan", "notas" => [5, 9, 8]],
["nombre" => "María", "notas" => [6, 6, 7]]
];
```
Calcula y muestra la nota media de cada estudiante.

5. Crea un array asociativo con 5 países como claves y sus capitales como valores. Después, escribe código que compruebe si "España" existe como clave y, si no existe, la añada con valor "Madrid".

6. Dado un array de números, escribe código que divida el array en dos arrays: uno con números pares y otro con impares.

7. Crea una función que reciba dos arrays y devuelva un nuevo array con los elementos que están en el primer array pero no en el segundo.

8. Dado el siguiente array multidimensional:
```php
$inventario = [
"frutas" => ["manzana" => 5, "pera" => 3],
"verduras" => ["zanahoria" => 8, "tomate" => 4]
];
```
Escribe código que incremente en 2 todas las cantidades y muestre el array actualizado.

9. Crea una función que "aplane" un array multidimensional de cualquier profundidad en un array unidimensional.

10. Dado un array de palabras, crea un array asociativo donde las claves sean las palabras y los valores sean la cantidad de veces que aparece cada palabra.

